class demo {
    public static void main(String[] args) {
        /* comparison operator */
        int x = 10;
        int y = 20;

        System.out.println(x<y);
        System.out.println(x<=5);

        System.out.println(y>50);
        System.out.println(y>=20);

        System.out.println(x==y);
        System.out.println(x!=y);
        System.out.println(x==10);
        System.out.println(x!=10);
    
    }
    
}
